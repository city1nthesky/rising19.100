#ifndef _RSVM_RESULT_H_
#define _RSVM_RESULT_H_

#define RR_DEF_FAIL_RC(x)		((x) | 0x80000000)
#define RR_FAILED(x)			((x) & 0x80000000)
#define RR_SUCCEED(x)			(!RR_FAILED((x)))

#define RR_OK				0x00000000
#define RR_FAIL				0xffffffff
#define RR_UNSUPPORTED			0xfffffffe
#define RR_INVALID_PARAM		RR_DEF_FAIL_RC(1)
#define RR_INTERN_ERROR			RR_DEF_FAIL_RC(2)
#define RR_INVALID_INST			RR_DEF_FAIL_RC(10)
#define RR_INVALID_TYPE			RR_DEF_FAIL_RC(11)
#define RR_INVALID_ID			RR_DEF_FAIL_RC(12)
#define RR_INVALID_METHOD		RR_DEF_FAIL_RC(13)
#define RR_INVALID_DB			RR_DEF_FAIL_RC(14)
#define RR_INVALID_PATH			RR_DEF_FAIL_RC(15)
#define RR_INVALID_FORMAT		RR_DEF_FAIL_RC(16)
#define RR_INVALID_ADDR			RR_DEF_FAIL_RC(17)
#define RR_INVALID_IMAGE		RR_DEF_FAIL_RC(18)
#define RR_CANNOT_CREAT_FS		RR_DEF_FAIL_RC(100)
#define RR_LACK_OF_RESOURCE		RR_DEF_FAIL_RC(200)
#define RR_NOT_ENOUGH_STACK		RR_DEF_FAIL_RC(201)
#define RR_EXCEPTION_UNHANDLED		RR_DEF_FAIL_RC(300)

#define RR_ALREADY_EXIST		1

#endif /* _RSVM_RESULT_H_ */
